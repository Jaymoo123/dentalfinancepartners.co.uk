def update_data_ts_with_hero_images(hero_map: dict[str, dict]) -> int:
    """Patch data.ts so that each CityData entry gains a `heroImage` field
    plus the type adds it. Idempotent: re-running replaces previous values."""
    text = DATA_TS.read_text(encoding="utf-8")

    # 1. Ensure the CityData type declares heroImage. If already present, leave.
    if "heroImage:" not in text.split("export const CITIES")[0]:
        # Inject before the closing brace of `CityData` type. Locate the
        # `adjacentTown: { slug: string; name: string };` line and add after it.
        text = re.sub(
            r"(adjacentTown: \{ slug: string; name: string \};\s*\n)",
            r"\1  heroImage: {\n    url: string;\n    alt: string;\n    photographer: string;\n    photographer_url: string;\n    pexels_url: string;\n  };\n",
            text,
            count=1,
        )

    # 2. For each city, append a heroImage block before the closing `},` of its
    #    entry. Use the unique `"slug": "xxxx"` opening to find the block,
    #    then scan forward to the next `},\n` at the top object level.
    updated = 0
    for slug, hero in hero_map.items():
        if not hero:
            continue
        # Drop any existing heroImage line for this city first (idempotent)
        pattern_existing = re.compile(
            r'(  "' + re.escape(slug) + r'":\s*\{[\s\S]*?)\n    heroImage:\s*\{[\s\S]*?\},\n', re.MULTILINE
        )
        text = pattern_existing.sub(r"\1\n", text)

        # Find the city's adjacentTown line and inject heroImage immediately after it
        city_pattern = re.compile(
            r'(  "' + re.escape(slug) + r'":\s*\{[\s\S]*?adjacentTown:\s*\{[^}]*\},\n)', re.MULTILINE
        )

        def replace_in_city(m: re.Match) -> str:
            block = m.group(1)
            hero_lines = (
                "    heroImage: {\n"
                f"      url: {json.dumps(hero['public_url'])},\n"
                f"      alt: {json.dumps(hero['alt'])},\n"
                f"      photographer: {json.dumps(hero['photographer'])},\n"
                f"      photographer_url: {json.dumps(hero['photographer_url'])},\n"
                f"      pexels_url: {json.dumps(hero['pexels_url'])},\n"
                "    },\n"
            )
            return block + hero_lines

        new_text, n = city_pattern.subn(replace_in_city, text, count=1)
        if n > 0:
            text = new_text
            updated += 1
        else:
            print(f"  WARN: could not patch heroImage into '{slug}' (city block not found)")

    DATA_TS.write_text(text, encoding="utf-8")
    return updated


def load_city_list_from_data() -> list[tuple[str, str]]:
    """Read data.ts to find (slug, name) pairs."""
    text = DATA_TS.read_text(encoding="utf-8")
    pairs = re.findall(r'"(\w[\w-]*)":\s*\{\s*\n\s*slug:\s*"([\w-]+)",\s*\n\s*name:\s*"([^"]+)"', text)
    out = []
    for outer, slug, name in pairs:
        if outer == slug:
            out.append((slug, name))
    return out


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--only", help="fetch one specific slug")
    parser.add_argument("--skip-existing", action="store_true", help="don't refetch if file exists")
    parser.add_argument("--sleep", type=float, default=0.3, help="seconds between requests")
    args = parser.parse_args()

    if not PEXELS_KEY:
        sys.exit("PEXELS_API_KEY not set in .env")

    cities = load_city_list_from_data()
    if args.only:
        cities = [(s, n) for s, n in cities if s == args.only]

    print(f"Fetching hero images for {len(cities)} cities...")
    print()

    hero_map: dict[str, dict] = {}
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    for slug, name in cities:
        try:
            h = fetch_for_city(slug, name, args.skip_existing)
            if h:
                hero_map[slug] = h
                print(f"  [{slug:>16}]  ok  -> {h['public_url']}  ({h['photographer']})")
            time.sleep(args.sleep)
        except Exception as e:
            print(f"  [{slug:>16}]  ERROR {type(e).__name__}: {e}")

    if hero_map:
        print()
        print(f"Patching {len(hero_map)} heroImage entries into {DATA_TS.name}...")
        n = update_data_ts_with_hero_images(hero_map)
        print(f"  {n} entries patched")
    else:
        print("\nNo new images fetched; data.ts left as-is.")


if __name__ == "__main__":
    main()
