"""
Fetch a hero image for each city in locations/[slug]/data.ts from Pexels.

Saves images to web/public/locations/[slug].jpg and appends a heroImage
field to each CityData entry in the data.ts file.

Strategy:
  - For each city, search Pexels for "[city name] city / skyline / landmark"
  - Pick the first orientation:landscape result with decent size
  - Save photographer credit for attribution

Pexels licence allows free commercial use without attribution; we attach
photographer credit anyway as good practice.

Run:
    python pipeline/fetch_city_images.py             # fetch for all 30
    python pipeline/fetch_city_images.py --only bradford
    python pipeline/fetch_city_images.py --skip-existing  # don't refetch if file exists
"""
import argparse
import json
import os
import re
import sys
import time
from pathlib import Path

import httpx

ROOT = Path(__file__).resolve().parents[1]
DATA_TS = ROOT / "web" / "src" / "app" / "locations" / "[slug]" / "data.ts"
OUT_DIR = ROOT / "web" / "public" / "locations"

try:
    from dotenv import load_dotenv
    load_dotenv(Path(__file__).resolve().parents[2] / ".env", override=True)
except ImportError:
    pass

PEXELS_KEY = os.getenv("PEXELS_API_KEY")


# City -> search query overrides for cities where the plain name returns
# ambiguous results (e.g. "Reading" returns books).
QUERY_OVERRIDES = {
    "reading": "reading berkshire town",
    "york": "york england minster",
    "newcastle": "newcastle upon tyne quayside",
    "cambridge": "cambridge england university",
    "oxford": "oxford england spires",
    "hull": "hull east yorkshire marina",
    "brighton": "brighton seafront pier",
    "bournemouth": "bournemouth beach",
    "plymouth": "plymouth devon harbour",
    "portsmouth": "portsmouth spinnaker tower",
    "stoke-on-trent": "stoke on trent staffordshire",
    "milton-keynes": "milton keynes uk centre",
}


def pexels_search(query: str, per_page: int = 5) -> list[dict]:
    if not PEXELS_KEY:
        raise RuntimeError("PEXELS_API_KEY not set")
    r = httpx.get(
        "https://api.pexels.com/v1/search",
        headers={"Authorization": PEXELS_KEY},
        params={
            "query": query,
            "per_page": per_page,
            "orientation": "landscape",
            "size": "large",
        },
        timeout=20.0,
    )
    r.raise_for_status()
    return r.json().get("photos", [])


def download(url: str, dest: Path) -> int:
    r = httpx.get(url, timeout=30.0)
    r.raise_for_status()
    dest.parent.mkdir(parents=True, exist_ok=True)
    dest.write_bytes(r.content)
    return len(r.content)


def fetch_for_city(slug: str, name: str, skip_existing: bool) -> dict | None:
    out_path = OUT_DIR / f"{slug}.jpg"
    if skip_existing and out_path.exists():
        print(f"  [{slug}]  exists, skipping")
        return None

    query = QUERY_OVERRIDES.get(slug, f"{name} city uk")
    photos = pexels_search(query, per_page=5)
    if not photos:
        # Fallback: try shorter
        photos = pexels_search(name, per_page=5)
    if not photos:
        print(f"  [{slug}]  no photos found for query {query!r}")
        return None

    # Pick the first landscape, large-enough photo (Pexels returns landscape
    # already, but we double-check dimensions).
    pick = None
    for p in photos:
        w = p.get("width", 0)
        h = p.get("height", 0)
        if w >= 1200 and w >= h:
            pick = p
            break
    if pick is None:
        pick = photos[0]

    src_url = pick["src"]["landscape"] or pick["src"]["large2x"] or pick["src"]["original"]
    download(src_url, out_path)

    return {
        "public_url": f"/locations/{slug}.jpg",
        "photographer": pick.get("photographer", ""),
        "photographer_url": pick.get("photographer_url", ""),
        "pexels_url": pick.get("url", ""),
        "alt": pick.get("alt", f"{name} city scene"),
    }


